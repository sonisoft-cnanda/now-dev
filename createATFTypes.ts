// Function to generate TypeScript interfaces and classes
function generateModels() {
  const tables = sysDbObject.map(table => table.name);
  const columns = sysDictionary.reduce((acc, curr) => {
    if (!acc[curr.name]) {
      acc[curr.name] = [];
    }
    acc[curr.name].push({ column: curr.element, type: curr.internal_type });
    return acc;
  }, {});

  tables.forEach(table => {
    const interfaceName = `I${capitalize(table.replace(/_/g, ''))}`;
    const className = `${capitalize(table.replace(/_/g, ''))}`;
    const classProperties = columns[table].map(col => `private _${col.column}: ${mapType(col.type)};`).join('\n    ');
    const gettersSetters = columns[table].map(col => `
    get ${camelCase(col.column)}(): ${mapType(col.type)} {
        return this._${col.column};
    }

    set ${camelCase(col.column)}(value: ${mapType(col.type)}) {
        this._${col.column} = value;
    }`).join('\n');

    console.log(`export interface ${interfaceName} {`);
    columns[table].forEach(col => {
      console.log(`    get ${camelCase(col.column)}(): ${mapType(col.type)};`);
      console.log(`    set ${camelCase(col.column)}(value: ${mapType(col.type)});`);
    });
    console.log(`}\n`);

    console.log(`export class ${className} implements ${interfaceName} {`);
    console.log(`    ${classProperties}\n`);
    console.log(`${gettersSetters}`);
    console.log(`}\n`);
  });
}

function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function camelCase(str: string): string {
  return str.replace(/_([a-z])/g, g => g[1].toUpperCase());
}

function mapType(type: string): string {
  const typeMap:any = {
    "string": "string",
    "reference": "ReferenceLink",
    "GUID":"string",
    "choice":"string",
    "boolean":"boolean",
    "integer":"number",
    "compressed":"string",
    "counter":"number",
    "glide_date_time":"Date",
    "glide_duration":"string",
    "sys_class_name":"string",
    "glide_precise_time":"string",
    "glide_var":"string",
    "collection":""

  };
  return typeMap[type] || "any";
}
